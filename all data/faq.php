  <div class="container">
    <p><strong>Q: I sent funds from an exchange or other third party to my MyNXT wallet, but they aren&#8217;t showing up. Why not?<br /></strong> A: All NXT transactions are publicly visible on the blockchain. Check the <a href="http://mynxt.info/blockexplorer/index.php">blockchain explorer</a> to find out whether your NXT has been sent by entering the address of your MyNXT wallet. If it does not show up there, then the exchange or third party has not sent it yet.<br />
    <br />
    Exchanges have different withdrawal policies and may require you to wait for a certain amount of time or confirm the withdrawal via an email link before processing it. If you are concerned, open a support ticket or contact your exchange. The blockchain explorer can be used to see whether your payment has been processed, so you will always be able to prove if your transaction has been delayed.</p>
    <p><strong>Q: I opened my MyNXT wallet and all my funds are gone! What happened?<br /></strong> A: The contents of every address can be viewed on the blockchain. If you cannot see your NXT in your MyNXT wallet, use the <a href="http://mynxt.info/blockexplorer/index.php">blockchain explorer</a> to check your account. It is possible that there is simply a temporary problem with our servers. The wallet and the block explorer use different servers, so if your NXT has been withdrawn from your account, you will be able to see where it has been transferred.</p>
    <p><strong>Q: Why would I use the MyNXT wallet instead of the downloadable NXT client?<br /></strong> A: The downloadable client can only be used to access your NXT from the computer on which the software is running. MyNXT.info offers broadly the same functionality as the client but with the convenience of a web wallet. You will be able to access your account from any device, including a tablet or smartphone, without downloading the client for each one. Additionally, we offer a high level of security, requiring separate login and wallet passwords rather than using a single passphrase, as the standard client does.</p>
    <p><strong>Q: Do you offer an API?</strong><br />
    A: Yes. The MyNXT.info API enables you to interact with your MyNXT wallet, as well as perform standard NXT queries. We run a network of at least 10 high-performance NRS servers, monitored 24/7 and spread around the world. When you make a call to the MyNXT API, we automatically determine the best server to connect you to in order to process your request. You can <a href="http://mynxt.info/apidocs/index.php">find out more about the API here</a>.</p>
    <p><strong>Q: Is my wallet secure?<br /></strong> A: MyNXT.info takes security very seriously. Your wallet is encrypted twice using state-of-the-art AES-256 encryption. Decryption happens client-side, so we never have access to your wallet. We also encrypt your wallet ourselves to provide another layer of security, and you will be notified via email of any activity on your account. You can read more about our security measures here.</p>
    <p><strong>Q: Can MyNXT access my funds?</strong><br />
    A: No. As well as requiring a password to log in to the site, your wallet is protected by a second, longer password. Only someone who has this second password can make transactions from your wallet. Because your wallet is decrypted client-side, MyNXT.info can never access your funds.</p>
    <p><br />
