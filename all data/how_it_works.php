      <h2><strong>How it works</strong><br />
      <small>How the MyNXT Wallet works</small><br /></h2>
    </div>
  </div>
  <div class="container">
    <p>MyNXT.info gives you the security of a downloadable client with the convenience of a web wallet. Users own their wallets, but do not have to download the blockchain or any other software! At no point does MyNXT have access to your funds.</p>
    <p><strong>Sign up<br /></strong> When you first sign up, you&#8217;ll be asked to enter a login password. This will give you access to the site.</p>
    <p><strong>Create a wallet<br /></strong> You&#8217;ll also be prompted to create a wallet password. This is a longer password that will be used to generate your NXT account number and access it. No one can make a transaction from your account without this password.</p>
    <p><strong>Browser encryption<br /></strong> Your wallet is encrypted client-side &#8211; that is, in your browser &#8211; using your wallet password. This means we can never gain access to your NXT.</p>
    <p><strong>Cloud storage<br /></strong> The encrypted details for your NXT account are saved on our servers. Whenever you want to view your account or make a transaction, all you have to do is log in.</p>
    <p><strong>No blockchain download</strong><br />
    Once you&#8217;ve logged in to our website, you use the NXT API to send NXT or make other transactions using your wallet password. That means there&#8217;s no need to download the blockchain or the standard client &#8211; so you can use your wallet from any computer or even a smartphone.</p>
    <p><strong>You own your wallet<br /></strong> Users have full ownership of their wallets. MyNXT.info cannot access your details, since all decryption happens client-side. (This also means you need to take good care of your password, since we cannot send you a replacement if you lose it!) If you want, you can download your wallet as a text file and keep it on your own computer, too, for extra safety. It&#8217;s your NXT &#8211; you should have the freedom to use it as you wish.</p>
